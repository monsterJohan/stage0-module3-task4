package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char c = 'c';
        char h = 'h';
        char a = 'a';
        char r = 'r';

        System.out.println((int)c);
        System.out.println((int)h);
        System.out.println((int)a);
        System.out.println((int)r);
    }
}
